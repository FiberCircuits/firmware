// Function to scan the array in a zigzag way instead of raster order:
void scanArrayZigzag(int x_max, int y_max, int array[y_max][x_max]) {
    int x = 0, y = 0;

    for (int i = 0; i < x_max * y_max; i++) {
        int idx = x + y * x_max;
        printf("%02d,%02d:%02d, ", x, y, idx);
     // strip.setPixelColor(idx, strip.Color(0, 0, 90));

        if (y % 2 == 0) {               // scan right in even rows
            if (x == x_max - 1) y++;    // go to next row if reached the end
            else                x++;
        } else {                        // scan left in odd rows
            if (x == 0) y++;            // go to next row if reached the end
            else        x--;            // otherwise go to "next" pixel
        }
    }
    printf("\n");
  //strip.show();
}


